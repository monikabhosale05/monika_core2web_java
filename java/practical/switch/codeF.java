class Num{
	public static void main(String[] args){
		int num=6;
		if(num==1){
			System.out.println("one");
		}
		else if(num==2){
			System.out.println("two");
		}
		else if(num==3){
			System.out.println("three");
		}
		else if(num==4){
			System.out.println("four");
		}
		else if(num==5){
			System.out.println("five");
		}
		else{
			System.out.println("Number is greater than 5");
		}
	}
}
