class Ladder{
	public static void main(String[] args){
		char ch='O';
		switch(ch){
			case 'A':
				System.out.println("A grade");
				break;
			case 'B':
				System.out.println("B grade");
				break;
			case 'O':
				System.out.println("Outstanding");
				break;
			case 'D':
				System.out.println("D grade");
				break;
		}
	}
}
